package ejercicio16;

public class Matematica {
    
    private Double n1;
    private Double n2;

    public Matematica() {
        this.n1=0.0;
        this.n2=0.0;
    }

    public Matematica(Double n1, Double n2) {
        this.n1 = n1;
        this.n2 = n2;
    }

    public Double getN1() {
        return n1;
    }

    public void setN1(Double n1) {
        this.n1 = n1;
    }

    public Double getN2() {
        return n2;
    }

    public void setN2(Double n2) {
        this.n2 = n2;
    }

   
    
    
    

}
